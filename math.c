#include<stdio.h>

int main() {
     // Arithmatic Operators Like - ( + , - , * , /)
     int x;
     int y;
     printf("Enter A Number :- ");
     scanf("%d",&x);
     printf("Enter A Number :- ");
     scanf("%d",&y);
     printf("\n%d + %d = %d", x , y , x + y );
     printf("\n%d - %d = %d", x , y , x - y );
     printf("\n%d * %d = %d", x , y , x * y );
     printf("\n%d / %d = %d", x , y , x / y );
}